// Operadores Aritmeticos

console.log('Adição '+ (5+6))//Adição
console.log("Subtração " + (158-65))//Subtração
console.log("Multiplicação " + (65*3))//Multiplicação
console.log("Divisão " + (60/3))//Divisão
console.log("Resto da Divisão " + (60%3))//Resto da Divisão
console.log("Exponecial "+ (9**3))//Exponecial

//Incremento
let inc = 0
console.log(inc++)//Depois vai ter o Incremento
console.log(++inc)//Antes já teve o Incremento
//Decremento
let dec = 0
console.log(dec--)//Depos vai ter o Decremento
console.log(--dec)//Antes já ocorreu o Decremento
